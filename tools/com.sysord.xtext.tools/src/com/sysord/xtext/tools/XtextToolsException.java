package com.sysord.xtext.tools;

public class XtextToolsException extends Exception {

	public XtextToolsException() {
		super();
	}

	public XtextToolsException(String message, Throwable cause) {
		super(message, cause);
	}

	public XtextToolsException(String message) {
		super(message);
	}

	public XtextToolsException(Throwable cause) {
		super(cause);
	}

}
