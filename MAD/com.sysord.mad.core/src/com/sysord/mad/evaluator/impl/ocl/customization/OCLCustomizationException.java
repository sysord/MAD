package com.sysord.mad.evaluator.impl.ocl.customization;

public class OCLCustomizationException extends Exception {

	public OCLCustomizationException() {
		super();
	}

	public OCLCustomizationException(String message, Throwable cause) {
		super(message, cause);
	}

	public OCLCustomizationException(String message) {
		super(message);
	}

	public OCLCustomizationException(Throwable cause) {
		super(cause);
	}

}
